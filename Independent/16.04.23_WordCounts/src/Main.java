import java.util.*;
import java.lang.*;

class Main
{
    public static void main (String[] args) throws java.lang.Exception
    {
        Scanner sc = new Scanner(System.in);

        int current = sc.nextInt();
        while(current != 42){

            System.out.println(current);
            current = sc.nextInt();
        }
    }
}